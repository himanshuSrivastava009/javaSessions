package JavaSessions;

public class TwoForLoops {

	public static void main(String[] args) {

		//9 x 9 = 81
		//m x n 
		// 00 01 02 03
		// 10 11 12 13
		for(int i=0; i<=3; i++){
			
			for(int j=0; j<=3; j++){
				
				System.out.print(i+""+j+" ");//00 
				
			}
			System.out.println();
			
			
		}
		
		
		
	}

}
